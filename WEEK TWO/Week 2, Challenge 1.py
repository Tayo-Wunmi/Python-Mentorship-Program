#CHALLENGE:
#Write a short program that prints the numbers 1 to 10 using a for loop.
# Then write an equivalent program that prints the numbers 10 to 1 using a while loop.

#SOLUTION

# 1. FOR LOOP
for i in range(1,11):
    print(i)


# 2. WHILE LOOP

number = 1
while number <= 10:
    print(number)
    number += 1

